package com.akshay.answers;

public class Pair {
	double first;
	int second;

	public Pair(double first, int second) {
		this.first = first;
		this.second = second;
	}

	public Pair() {

	}

	public double getFirst() {
		return first;
	}

	public void setFirst(double first) {
		this.first = first;
	}

	public int getSecond() {
		return second;
	}

	public void setSecond(int second) {
		this.second = second;
	}

}
